#Faça um programa que leia do usuário um número e escreva o seu
#sucessor e o seu antecessor.

print("----Imprimir Antecessor e Sucessor----")

num = int(input("Informe um número: "))

antecessor = num - 1
sucessor = num + 1

print("O antecessor do número ", num, " é ", antecessor, ", e o sucessor é ", sucessor)